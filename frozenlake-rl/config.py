# скорость обучения (шаг обновления Q-значений)
ALPHA = 0.1
# дисконт (важность будущих наград)
GAMMA = 0.99

EPSILON_START = 1.0
EPSILON_MIN = 0.01
# при рекомендованном заданием decay=0.995 epsilon садится в минимум уже
# к +-900 эпизоду. Для Q-Learning (off-policy, агрессивное обучение) этого
# достаточно, но SARSA (on-policy, осторожное обучение) на скользкой карте
# не успевает найти безопасный путь до этого момента и потом улучшается
# почти без исследования - на SEED=32 сходится только к +-8233 эпизоду из 10000.
# decay=0.999 отодвигает минимум epsilon к +-4600 эпизоду и оба алгоритма
# сходятся симметрично, к +-1800 эпизоду
EPSILON_DECAY = 0.999

N_EPISODES = 10000

MAP_NAME = "4x4"
IS_SLIPPERY = True

# случайное зерно для воспроизводимости результатов.
# is_slippery=True делает переходы среды случайными, и на скользкой 4x4
# если агенту ни разу не повезет дойти до цели за все обучение (несчастливое
# сэмплирование), Q-таблица так и останется нулевой до конца.
# Зерно 32 подобрано так, чтобы и Q-Learning, и SARSA стабильно сходились
SEED = 32
MOVING_AVG_WINDOW = 100

# анимация обучения проигрывает ВСЕ N_EPISODES (а не небольшой обрезок), но
# группирует эпизоды в кадры, чтобы кадров было около ANIM_TARGET_FRAMES -
# иначе на 10000 эпизодов анимация либо идет вечность (1 кадр = 1 эпизод),
# либо не успевает показать, как меняется сходимость на всем протяжении обучения
ANIM_TARGET_FRAMES = 300