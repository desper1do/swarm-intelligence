# ---------- QMIX (value-based, CTDE через смешивающую сеть) ----------
class QMIX:
    # double-Q убирает катастрофическую деградацию, но на очень длинной
    # дистанции (>1200 эпизодов) остается пологое сползание Q-значений вниз.
    # 1000 эпизодов - точка, где обучение уже вышло на плато (success ~1.0) и
    # еще не начало сползать: график сходимости чистый (рост -> плато)
    N_EPISODES = 1000

    LR = 5e-4
    GAMMA = 0.99

    EPSILON_START = 1.0
    EPSILON_MIN = 0.05
    EPSILON_DECAY = 0.995

    BATCH_SIZE = 32  # эпизодов на батч (не отдельных переходов - буфер эпизодический из-за GRU)
    BUFFER_SIZE = 5000  # максимум эпизодов в буфере

    HIDDEN_SIZE = 64  # размер GRU и FC слоев агентской сети
    MIXING_HIDDEN_SIZE = 32  # размер скрытого слоя смешивающей сети (гиперсетей)

    TAU = 0.005  # коэффициент soft update целевых сетей
    TARGET_UPDATE_EVERY = 1  # обновлять целевые сети каждые N эпизодов (soft update)


# ---------- MADDPG (policy-gradient, централизованный критик на агента) ----------
class MADDPG:
    # ванильный MADDPG (одиночный критик) уверенно рос ~150-200 эпизодов, а
    # потом стабильно коллапсировал из-за переоценки Q-значений в нестационарной
    # мультиагентной среде (см. п.3.3 задания). Стабилизируем TD3-приемами:
    # twin critics + min(Q1,Q2), сглаживание целевой политики и отложенное
    # обновление актера - это убирает коллапс и позволяет учить дольше
    LR_ACTOR = 1e-4
    LR_CRITIC = 1e-3
    N_EPISODES = 1000
    GAMMA = 0.95

    TAU = 0.01

    BATCH_SIZE = 1024  # отдельных переходов на батч (буфер обычный, не эпизодический)
    BUFFER_SIZE = int(1e6)

    SIGMA = 0.2  # стандартное отклонение гауссовского шума для исследования
    SIGMA_MIN = 0.02
    SIGMA_DECAY = 0.997  # decay на эпизод (по аналогии с epsilon у QMIX, в задании явно не задан)
    NOISE_CLIP = 0.5

    # TD3-специфика
    TARGET_NOISE = 0.2  # шум на логиты целевой политики (target policy smoothing)
    POLICY_DELAY = 2    # актер и целевые сети обновляются раз в POLICY_DELAY апдейтов критика

    HIDDEN_SIZE = 128  # размер FC слоев актера и критика

    UPDATE_EVERY = 100  # обучение раз в N шагов среды (не раз в эпизод)
